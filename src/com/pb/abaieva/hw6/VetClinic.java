package com.pb.abaieva.hw6;

import java.lang.reflect.Constructor;

public class VetClinic {

    public static void main(String[] args) throws Exception {

        Animal[] patientsVC = new Animal[3];

        Dog rover = new Dog();
        rover.food = "премиум корм";
        rover.location = "конура";
        rover.setNeedsWalk(false);

        Cat tom = new Cat();
        tom.food = "сырая рыба";
        tom.location = "подвал";
        tom.setClawSharpness("острые");

        Horse bucephalus = new Horse();
        bucephalus.food = "свежее сено";
        bucephalus.location = "конюшня";
        bucephalus.setIsMounted(false);

        patientsVC[0] = rover;
        patientsVC[1] = tom;
        patientsVC[2] = bucephalus;

        for (int i = 0; i < patientsVC.length; i++) {

            Class vetClass = Class.forName("com.pb.abaieva.hw6.Veterenarian");
            Constructor constr = vetClass.getConstructor(new Class[] {});

            Object obj = constr.newInstance();

            if (obj instanceof Veterenarian) {
                ((Veterenarian) obj).treatAnimal(patientsVC[i]);
            }

        }
    }
}

